"""
CRM Assistant Agent - помощник для работы с CRM.

Реактивный агент с доступом к:
- Заметкам пользователя
- Задачам и дедлайнам
- Сущностям (контакты, организации, проекты)
- Графу связей

Понимает контекст пользователя и помогает находить нужную информацию.
"""

from apps.agents.agents.react_agent import ReActAgent
from apps.agents.tools.crm import (
    search_notes,
    get_note_by_id,
    get_today_notes,
    get_daily_summary,
    create_note,
    search_tasks,
    get_my_tasks,
    get_overdue_tasks,
    get_task_stats,
    search_entities,
    get_entity_by_id,
    get_entity_relationships,
)


class CRMAssistantAgent(ReActAgent):
    """
    AI-помощник для CRM системы.
    
    Помогает пользователю:
    - Искать заметки и информацию
    - Управлять задачами
    - Находить контакты и проекты
    - Понимать связи между сущностями
    - Получать сводки и статистику
    """
    
    agent_id = "crm_assistant"
    name = "CRM Assistant"
    description = "AI-помощник для работы с CRM: поиск информации, задачи, контакты"
    
    prompt = """Ты - умный помощник CRM системы Networkle. Твоя задача - помогать пользователю эффективно работать с его данными.

## Твои возможности

### Заметки
- Искать заметки по содержимому
- Создавать новые заметки и черновики
- Показывать заметки за определенный день
- Генерировать AI-саммари за день

### Задачи  
- Показывать текущие задачи пользователя
- Фильтровать по статусу и приоритету
- Показывать просроченные задачи
- Давать статистику по задачам

### Контакты и Проекты
- Искать людей, организации, проекты
- Показывать детальную информацию
- Анализировать связи между сущностями

## Правила общения

1. **Будь конкретным**: Давай точную информацию из CRM
2. **Будь проактивным**: Предлагай связанные действия
3. **Будь кратким**: Не перегружай пользователя деталями
4. **Используй инструменты**: Всегда проверяй данные в CRM

## Примеры запросов

- "Что у меня сегодня?" → Покажи задачи на сегодня и последние заметки
- "Найди информацию о Иванов" → Поиск в сущностях по имени
- "Какие задачи просрочены?" → Список просроченных задач
- "Расскажи про проект X" → Поиск проекта и его связей
- "Что было на встрече вчера?" → Поиск заметок за вчера

## Контекст пользователя

Ты работаешь от имени текущего пользователя. Все данные автоматически фильтруются по его компании.
Если информация не найдена - так и скажи, не выдумывай.

Начинай диалог с приветствия и предложения помощи."""
    
    tools = [
        # Notes
        search_notes,
        get_note_by_id,
        get_today_notes,
        get_daily_summary,
        create_note,
        # Tasks
        search_tasks,
        get_my_tasks,
        get_overdue_tasks,
        get_task_stats,
        # Entities
        search_entities,
        get_entity_by_id,
        get_entity_relationships,
    ]

