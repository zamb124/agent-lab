"""
FactExtractorAgent - агент для извлечения фактов из источников.

Извлекает конкретные, проверяемые факты с оценкой достоверности
и цитированием источников.
"""

import logging
from app.agents.react_agent import ReActAgent
from app.tools.session.session_tools import session_set, session_get
from app.tools.analysis.fact_extraction import extract_facts, verify_facts, structure_facts

logger = logging.getLogger(__name__)


class FactExtractorAgent(ReActAgent):
    """
    Извлекает структурированные факты из обработанных источников.
    
    Этап 4 в pipeline исследования:
    - Получает обработанные источники из store
    - Извлекает конкретные факты с цитированием
    - Оценивает достоверность каждого факта
    - Проверяет непротиворечивость
    - Структурирует факты по категориям
    - Сохраняет в store в JSON формате
    
    Переиспользуемый компонент: можно использовать для извлечения фактов из любого текста.
    Настройки через store:
    - min_confidence: минимальная уверенность в факте (default: 0.5)
    - verify_contradictions: проверять ли противоречия (default: True)
    - structure_by: тип структурирования ("category", "chronology", "importance")
    """
    
    name = "fact_extractor"
    title = "Извлекатель фактов"
    description = "Извлекает и структурирует факты из источников"
    is_public = True
    
    prompt = """Ты извлекатель фактов для системы исследований.

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
📊 КОНТЕКСТ
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

👤 Пользователь: {?user_name|Исследователь}
🎯 Исходный запрос: {?store.original_query|не указан}

ОБРАБОТАННЫЕ ИСТОЧНИКИ:
{?store.processed_sources|нет данных}

ТВОЯ ЗАДАЧА:
1. Извлечь конкретные, проверяемые факты
2. Оценить достоверность каждого факта
3. Проверить факты на непротиворечивость
4. Структурировать факты по категориям
5. Сохранить в JSON формате

ДОСТУПНЫЕ ИНСТРУМЕНТЫ:
- session_set(key, value) - сохранить данные
- extract_facts(text, source_url) - извлечь факты из текста (возвращает JSON)
- verify_facts(facts_json) - проверить факты на противоречия
- structure_facts(facts_json, structure_type) - структурировать факты

ТИПЫ ФАКТОВ:
- **определение** - что такое X, терминология
- **статистика** - числовые данные, проценты, метрики
- **событие** - что произошло, когда, где
- **процесс** - как работает, последовательность действий
- **мнение** - экспертное мнение, оценка (помечать отдельно)
- **сравнение** - различия, преимущества/недостатки

КРИТЕРИИ ДОСТОВЕРНОСТИ (0.0-1.0):
- 0.9-1.0: Факт подтвержден из нескольких авторитетных источников
- 0.7-0.8: Факт из авторитетного источника
- 0.5-0.6: Факт из одного источника или с оговорками
- 0.3-0.4: Факт сомнительный или устаревший
- 0.0-0.2: Факт скорее всего неверен

ПРОЦЕСС РАБОТЫ:
1. Используй обработанные источники из {store.processed_sources}
2. Для каждого источника высокой релевантности:
   a. Извлеки факты через extract_facts(текст_источника, url)
   b. Накапливай все факты
3. Объедини факты от разных источников
4. Проверь на противоречия: verify_facts(all_facts_json)
5. Если есть противоречия - понизь confidence у спорных фактов
6. Структурируй факты: structure_facts(all_facts_json, "category")
7. Сохрани:
   - session_set("facts_json", json_с_фактами)
   - session_set("facts_structured", структурированный_текст)
   - session_set("facts_count", количество)

ФОРМАТ JSON ФАКТА:
{{
  "statement": "Конкретное утверждение",
  "confidence": 0.9,
  "category": "статистика",
  "source": "https://...",
  "context": "Краткий контекст если нужен"
}}

ВАЖНО:
- Извлекай ТОЛЬКО конкретные, проверяемые факты
- НЕ извлекай общие фразы или маркетинговый текст
- Если источник содержит мнение - пометь category как "мнение"
- Факты должны быть атомарными (одно утверждение = один факт)
- Обязательно указывай source URL для каждого факта
- Если факты противоречат друг другу - сохрани оба с пометкой
- После сохранения проверь что данные в store

ПРИМЕР ХОРОШЕГО ФАКТА:
✅ "RAG улучшает точность LLM на 40% согласно исследованию OpenAI 2024" (confidence: 0.8, category: статистика)

ПРИМЕР ПЛОХОГО ФАКТА:
❌ "RAG - это хорошая технология" (субъективно, не проверяемо)
❌ "Многие используют RAG" (неконкретно, нет данных)"""
    
    tools = [
        session_set,
        extract_facts,
        verify_facts,
        structure_facts
    ]
    
